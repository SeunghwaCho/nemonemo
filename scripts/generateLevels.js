function make20x20(id, name, emoji, rows) {
  // rows는 15x15를 20x20으로 패딩
  const solution = [];
  for (let r = 0; r < 20; r++) {
    const row = new Array(20).fill(0);
    if (r < rows.length && rows[r]) {
      for (let c = 0; c < Math.min(rows[r].length, 20); c++) {
        row[c + Math.floor((20 - rows[r].length) / 2)] = rows[r][c];
      }
    }
    solution.push(row);
  }
  return makeLevel(id, name, emoji, solution, 'expert');
}

